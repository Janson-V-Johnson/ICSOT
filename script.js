// ===== CSV LOADING =====
async function loadCSV() {
    try {
        const response = await fetch(SHEET_URL);
        const text = await response.text();
        const rows = text.split('\n').map(row => row.split(','));
        csvData = rows.slice(1).map(row => ({
            id: row[0]?.trim(),
            name: row[1]?.trim(),
            location: row[2]?.trim(),
            time: row[3]?.trim(),
            date: row[4]?.trim()
        }));
    } catch (error) {
        console.error('Error loading CSV:', error);
    }
}

